          <p>Chef Kurt Fleischfresser is gearing up for the battle of his career as he prepares to tackle a grueling 100
            course tasting event to help fight hunger in Oklahoma. The event, that Fleischfresser has coined “Culinaire
            For A Cause,” will consist of a record-breaking 100 course tasting event divided into 10 (ten), 10-course
            dining settings, and will be held July 21 -22, 2012 at Oklahoma City’s The Tasting Room at Will Rogers
            Theater.</p>

          <p>Fleischfresser will cook non-stop for 14 hours with dining sets running from 10:30 a.m. on July 21 to 1:30
            a.m. on July 22. This unique fundraising event to benefit the Regional Food Bank of Oklahoma will include
            food prepared by Chef Fleischfresser and his team, hand-selected wines to accompany each dining setting, and
            a culinary experience that will not be soon forgotten.</p>

          <p>For Fleischfresser, taking on this challenge was about one thing only, ending hunger in Oklahoma. “Every
            day close to 700,000 Oklahomans wake-up and go to bed hungry. I had the idea to do something like this from
            a sincere desire to be a part of a bigger solution to end hunger in this state,” said Fleischfresser. “I am
            very involved with the vision of the Regional Food Bank of Oklahoma and I’m always looking for creative ways
            to help this organization carry out its mission. When my team put together the idea for this event, I was
            like ‘absolutely’! It just felt right from the start.”</p>

          <p>Fleischfresser, who has spent nearly three decades as a chef, culinary instructor, and international
            culinary platform artist, is the mastermind behind more than 25 successful restaurant concepts in the state
            of Oklahoma that include: The Coach House, The Metro Wine Bar and Bistro, Earl’s Rib Palace, Portobello, The
            Tasting Room at Will Rogers Theater, The Deep Fork Grill and all seven locations of the Ground Floor
            Café.</p>

          <p>In 2009, Fleischfresser, whose restaurant group Western Concepts currently owns seven popular restaurants
            in the Oklahoma City area, joined the late Julia Child, Alan Wong from Hawaii and renowned celebrity chef
            Paul Prudhomme as the only four American chefs to receive France’s Medaille de Merite by L’Academie de
            Gastronomie Brillat-Savarin for his contributions in education in the culinary arts.</p>

          <p>According to Rodney Bivens, Executive Director of the Regional Food Bank of Oklahoma, this unique
            collaboration with Chef Fleischfresser is something that his organization hopes will encourage others to
            join the fight against hunger in Oklahoma. “Chef Fleischfresser has been a huge supporter of the Regional
            Food Bank for more than 25 years,” said Bivens. “His passion for our mission of ‘Fighting Hunger…Feeding
            Hope’ is unparalleled and we are grateful that he has chosen the Food Bank as the recipient charity for this
            incredible event!”</p>

